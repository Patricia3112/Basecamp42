char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	unsigned int i;
	unsigned int j;

	i = 0;
	j = 0;
	while (dest[i] != '\0')
		i++;
	while (src[j] != '\0' && nb > 0)
	{
		dest[j + i] = (unsigned char)src[j];
		j++;
		nb--;
	}
	dest[j + i] = '\0';
	return (dest);
}
